class string {
    public static void main(String[] args)
    {
        // Declare String without using new operator
        String s = "Hello";
 
        // Prints the String.
        System.out.println("String s = " + s);
 
        // Declare String using new operator
        String s1 = new String("World");
 
        // Prints the String.
        System.out.println("String s1 = " + s1);
    }
}